func partitionStringSlice(actions []string, batchSize int) [][]string {
	var batches [][]string

	for batchSize < len(actions) {
		batches = append(batches, actions[0:batchSize])
		actions = actions[batchSize:]
	}
	batches = append(batches, actions)

	return batches
}
